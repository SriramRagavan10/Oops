package oops1;

public class ToString {
	 String name;
	 int age;
	public String toString() {
		return "Name: "+name+" Age: "+age;
	}
	public void setName(String name) {
		this.name=name;
	}
	public void setAge(int age) {
		this.age=age;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	

}
